void MTDC32_SetWindowStart (int card, long n) {
	//===========================================================
  //==  
	//===========================================================
    mtdc32_ptr[card]->bank0_win_start = n;
    mtdc32_ptr[card]->bank1_win_start = n;
  return;
}

void MTDC32_SetWindowLength (int card, long n) {
	//===========================================================
  //==  
	//===========================================================
    mtdc32_ptr[card]->bank0_win_width = n;
    mtdc32_ptr[card]->bank1_win_width = n;
  return;
}

void MTDC32_SetResolution (int card, int n) {
	//===========================================================
  //==  
	//===========================================================
    mtdc32_ptr[card]->tdc_resolution = n;
  return;
}


void MTDC32_SetFirstHit (int card, int n) {
	//===========================================================
  //==  
	//===========================================================
    mtdc32_ptr[card]->first_hit = n;
  return;
}


